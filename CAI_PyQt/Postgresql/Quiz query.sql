DROP TABLE IF EXISTS CAI.TBL_QUIZSCORES;
CREATE TABLE CAI.TBL_QUIZSCORES (
    SCOREID        INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    QUIZNUMBER     INTEGER NOT NULL,
    LESSONID       INTEGER NOT NULL,
    GRADINGPERIOD  INTEGER NOT NULL,
    STUDENTID      VARCHAR(20) NOT NULL,
    SECTIONID      INTEGER NOT NULL,
    QUIZSCORE      VARCHAR(50) NOT NULL,
    ITEMS          VARCHAR(50) NOT NULL,
    DATETAKEN      TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP
);


DROP TABLE IF EXISTS CAI.TBL_QUIZSCORES_ARCHIVE;
CREATE TABLE CAI.TBL_QUIZSCORES_ARCHIVE (
    ARCHIVEID      INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    STUDENTID      VARCHAR(50) NOT NULL,
    SECTIONID      INTEGER NULL,
    LESSONID       INTEGER NOT NULL,
    QUIZNUMBER     INTEGER NULL,
    QUIZSCORE      VARCHAR(50) NULL,
    ITEMS          VARCHAR(50) NULL,
    GRADINGPERIOD  INTEGER NULL,
    DATETAKEN      TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP
);

DROP TABLE IF EXISTS cai.tbl_quizscores;
CREATE TABLE cai.tbl_quizscores (
	quiznumber    INTEGER NOT NULL,
	gradingperiod INTEGER NOT NULL,
	lessonid      INTEGER,
	studentid     VARCHAR(20) NOT NULL,
	quizscore     INTEGER NOT NULL, 
	totalitems    INTEGER NOT NULL,
	datetaken     TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE INDEX idx_student_progress ON cai.tbl_quizscores (studentid, quiznumber, gradingperiod, lessonid);
