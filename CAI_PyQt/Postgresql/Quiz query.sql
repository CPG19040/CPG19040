DROP TABLE IF EXISTS CAI.TBL_QUIZ;
CREATE TABLE IF NOT EXISTS CAI.TBL_QUIZ (
    QUIZKEY         INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    QUIZNUMBER      INTEGER NOT NULL,
    GRADINGPERIOD   INTEGER NOT NULL,
    LESSONID        INTEGER NOT NULL,
    TOTAL_ITEMS     INTEGER DEFAULT 0,
    QUIZSTATUS      VARCHAR(20),
    EASY_COUNT      INTEGER DEFAULT 0,
    AVERAGE_COUNT   INTEGER DEFAULT 0,
    HARD_COUNT      INTEGER DEFAULT 0,
    PUBLISH         BOOLEAN DEFAULT TRUE,
    CONSTRAINT UQ_QUIZ_IDENTITY UNIQUE (QUIZNUMBER, GRADINGPERIOD, LESSONID)
);


DROP TABLE IF EXISTS CAI.TBL_QUIZIDENTIFICATION;
CREATE TABLE CAI.TBL_QUIZIDENTIFICATION (
    IDKEY             INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    QUIZNUMBER        INTEGER NOT NULL,
    GRADINGPERIOD     INTEGER NOT NULL,
    LESSONID          INTEGER NOT NULL,
    DIFFICULTYLEVEL   INTEGER NOT NULL, -- 1. Easy, 2. Average, 3. Hard
    ITEMNO            INTEGER NOT NULL,
    QUESTION          TEXT NULL,
    CORRECT_ANSWER    TEXT NULL,
    IMAGEQUESTION     BYTEA NULL,
    CONSTRAINT UQ_ID_ITEM UNIQUE (QUIZNUMBER, GRADINGPERIOD, LESSONID, DIFFICULTYLEVEL, ITEMNO)
);


DROP TABLE IF EXISTS CAI.TBL_QUIZMULTIPLECHOICE;
CREATE TABLE CAI.TBL_QUIZMULTIPLECHOICE (
    MCKEY           INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    QUIZNUMBER      INTEGER NOT NULL,
    GRADINGPERIOD   INTEGER NOT NULL,
    LESSONID        INTEGER NOT NULL,
    DIFFICULTYLEVEL INTEGER NOT NULL, -- 1. Easy, 2. Average, 3. Hard
    ITEMNO          INTEGER NOT NULL,
    QUESTION        VARCHAR(100) NULL,
    CHOICE_A        VARCHAR(50) NOT NULL,
    CHOICE_B        VARCHAR(50) NOT NULL,
    CHOICE_C        VARCHAR(50) NOT NULL,
    CORRECT_ANSWER  VARCHAR(50) NOT NULL,
    IMAGEQUESTION   BYTEA NULL,
    CONSTRAINT UQ_MC_ITEM UNIQUE (QUIZNUMBER, GRADINGPERIOD, LESSONID, DIFFICULTYLEVEL, ITEMNO)
);


DROP TABLE IF EXISTS CAI.TBL_QUIZTRUEORFALSE;
CREATE TABLE CAI.TBL_QUIZTRUEORFALSE (
    TFKEY           INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    QUIZNUMBER      INTEGER NOT NULL,
    GRADINGPERIOD   INTEGER NOT NULL,
    LESSONID        INTEGER NOT NULL,
    DIFFICULTYLEVEL INTEGER NOT NULL, -- 1. Easy, 2. Average, 3. Hard
    ITEMNO          INTEGER NOT NULL,
    QUESTION        TEXT NULL,
    CORRECT_ANSWER  VARCHAR(50) NULL,
    IMAGEQUESTION   BYTEA NULL,
    CONSTRAINT UQ_TF_ITEM UNIQUE (QUIZNUMBER, GRADINGPERIOD, LESSONID, DIFFICULTYLEVEL, ITEMNO)
);


DROP TABLE IF EXISTS CAI.TBL_QUIZSCORES;
CREATE TABLE CAI.TBL_QUIZSCORES (
    SCOREID       INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
	QUIZNUMBER    INTEGER NOT NULL,
	GRADINGPERIOD INTEGER NOT NULL,
	LESSONID      INTEGER,
	STUDENTID     VARCHAR(20) NOT NULL,
	QUIZSCORE     INTEGER, 
	DATETAKEN     TIMESTAMP DEFAULT CURRENT_TIMESTAMP,

    CONSTRAINT UQ_QS_ITEM UNIQUE(QUIZNUMBER, GRADINGPERIOD, LESSONID, STUDENTID)
);

CREATE INDEX IDX_STUDENT_PROGRESS ON CAI.TBL_QUIZSCORES (QUIZNUMBER, GRADINGPERIOD, LESSONID, STUDENTID);


DROP TABLE IF EXISTS CAI.TBL_QUIZSCORES_ARCHIVE;
CREATE TABLE CAI.TBL_QUIZSCORES_ARCHIVE (
    ARCHIVEID      INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    QUIZNUMBER     INTEGER NULL,
    GRADINGPERIOD  INTEGER NULL,
    LESSONID       INTEGER NOT NULL,
    STUDENTID      VARCHAR(50) NOT NULL,
    QUIZSCORE      VARCHAR(50) NULL,
    DATETAKEN      TIMESTAMP NULL DEFAULT CURRENT_TIMESTAMP
);


