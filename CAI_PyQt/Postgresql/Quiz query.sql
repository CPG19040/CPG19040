DROP TABLE IF EXISTS CAI.TBL_QUIZ_TYPES;
CREATE TABLE IF NOT EXISTS CAI.TBL_QUIZ_TYPES (
    TYPEID      VARCHAR(10) PRIMARY KEY,        -- e.g., 'MC', 'TF', 'ID'
    TYPENAME    VARCHAR(50) NOT NULL UNIQUE     -- e.g., 'Multiple Choice', 'True/False'
);

