DROP TABLE IF EXISTS CAI.TBL_SCOREMULTIPLIER;
CREATE TABLE CAI.TBL_SCOREMULTIPLIER (
    MULTIPLIERID           INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    QUIZNUMBER             INTEGER NOT NULL,
    LESSONID               INTEGER NOT NULL,
    GRADINGPERIOD          INTEGER NOT NULL,
    EASY_MULTIPLIER        INTEGER DEFAULT 1,
    AVERAGE_MULTIPLIER     INTEGER DEFAULT 1,
    HARD_MULTIPLIER        INTEGER DEFAULT 1,
    TOTAL_POSSIBLE_POINTS  INTEGER DEFAULT 0,
    CONSTRAINT UQ_SCORE_MULTIPLIER UNIQUE (QUIZNUMBER, LESSONID, GRADINGPERIOD)
);
